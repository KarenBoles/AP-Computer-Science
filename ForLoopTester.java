package forLoopTester;

public class ForLoopTester {
	public static void main(String[] args) {
		for(int i=1; i<=8; i++)
			System.out.println(i);
		
		//for loops are used to repeat an action a certain number of iterations
		//The first item in for loop declaration declares and initializes the counting variable
		//The second item in the for loop declaration is a boolean test.  The loop will run as long as it is true.
		//The third item in the for loop declaration is the increment or decrement.  
		
		for(int j=25; j>=0;j-=5)
			System.out.println(j);
		
		
	}
}
